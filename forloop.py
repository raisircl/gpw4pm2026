# range function is used to generate a sequence of numbers. 
# It can take one, two, or three arguments. 
# The first argument is the starting number (inclusive), 
# the second argument is the ending number (exclusive), 
# and the third argument is the step size (optional).
# syntax: range(start, stop, step)
x=range(1,11,1) # generate numbers 1 to 9
for i in x:
    print(i) # print each number in the range

