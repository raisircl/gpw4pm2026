# one statement again and again
# Loop can be known or unknown 
# step 1: initalization of loop counter
# step 2: while loop condition:
# step 3: loop body
# step 4: increment or decrement of loop counter
# print hello user 5 times
i=1 # intialization of loop counter

while i<=5: # while loop condition
    print(f"{i}.Hello User") # loop body
    i=i+1 # increment of loop counter

